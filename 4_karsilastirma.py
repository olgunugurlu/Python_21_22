# ==, !=, <=, >=, <, >
sayi1 = 12
sayi2 = 9
#== eşit
#bu iki sayı birbirine eşit mi?
print(sayi1 == sayi2) 
#!= eşit değil
#bu iki sayı birbirine eşit değil mi?
print(sayi1 != sayi2) 
#küçük eşittir
print(sayi1 <= sayi2)
#sadece küçüktür
print(sayi1 < sayi2)
#büyük eşittir
print(sayi1 >= sayi2)
#büyüktür
print(sayi1 > sayi2)
